// Assignment operator
#include<iostream>
using namespace std;
int main(){
    int a =4,b=3;
    a=b;
    cout<<"The value of a :"<<a<<endl;
    a+=1;
    cout<<"The value of a :"<<a<<endl;
    a-=1;
    cout<<"The value of a :"<<a<<endl;
    a*=1;
    cout<<"The value of a :"<<a<<endl;
    a/=2;
    cout<<"The value of a :"<<a<<endl;
    return 0;
}