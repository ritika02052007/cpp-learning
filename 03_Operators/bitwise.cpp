//Bitwise Operator
#include<iostream>
using namespace std;
int main(){
    int a =4,b=8;
    cout<<"Bitwise And :"<<(a & b)<<endl;
    cout<<"Bitwise OR :"<<(a | b)<<endl;
    cout<<"Bitwise XOR :"<<(a ^ b)<<endl;
    cout<<"Bitwise Left Shift :"<<(a<<1)<<endl;
    cout<<"Bitwise Right Shift :"<<(a>>1)<<endl;
    return 0;
}